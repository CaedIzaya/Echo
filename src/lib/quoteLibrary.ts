/**
 * 随机获取一条文案
 * 先随机选择一个类别（A、B、C），然后从该类别中随机选择一条
 */
export function getRandomQuote(): string {
  const categories: QuoteCategory[] = ['A', 'B', 'C'];
  
  // 随机选择一个类别
  const randomCategory = categories[Math.floor(Math.random() * categories.length)];
  
  // 从选中的类别中随机选择一条文案
  const quotes = quoteLibrary[randomCategory];
  const randomIndex = Math.floor(Math.random() * quotes.length);
  
  return quotes[randomIndex];
}

/**
 * 获取指定类别的所有文案
 */
export function getQuotesByCategory(category: QuoteCategory): string[] {
  return quoteLibrary[category];
}

/**
 * 获取所有文案的总数
 */
export function getTotalQuoteCount(): number {
  return quoteLibrary.A.length + quoteLibrary.B.length + quoteLibrary.C.length;
}

