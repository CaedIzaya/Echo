// 从数组中随机取一句
function pickRandom(pool: string[]): string {
  if (!pool.length) return '';
  const idx = Math.floor(Math.random() * pool.length);
  return pool[idx];
}

// 心树基础心声（含三层觉察池，内部做轻权重随机）
export function getRandomHeartTreeMessage(): string {
  const r = Math.random();
  if (r < 0.6) {
    return pickRandom(BASE_AWARENESS_POOL);
  }
  if (r < 0.9) {
    return pickRandom(FORWARD_RECOGNITION_POOL);
  }
  return pickRandom(DEEP_INSIGHT_POOL);
}

export function getRandomWaterMessage(): string {
  return pickRandom(WATER_POOL);
}

export function getRandomFertilizeMessage(): string {
  return pickRandom(FERTILIZE_POOL);
}





















